
def area(a):
    '''
    Принимает :

        a(float): сторону квадрата

    Возвращаемое значение:

        float Площадь квадрата по формуле a^2

    Например:

        area(10) вернёт 100
    '''
    return a * a


def perimeter(a):
    '''
    Принимает :

        a(float): сторону квадрата

    Возвращаемое значение:

        float Периметр квадрата по формуле 4*a

    Например:

        perimeter(10) вернёт 40
    '''
    return 4 * a
